
import mongoose from "mongoose";

const userSchema= new mongoose.Schema({
    username:{
        type:String,
        required:true,
        unique:true,      
    },
    email:{
        type:String,
        required:true,
        unique:true,       
    },
    password:{
        type:String,
        required:true, 
    },
    avatar:{
        type:String,
        defaut:"https://img.freepik.com/premium-vector/default-avatar-profile-icon-social-media-user-image-gray-avatar-icon-blank-profile-silhouette-vector-illustration_561158-3383.jpg"
    },
},{timestamps:true});

const User = mongoose.model('User',userSchema);

export default User;